
package main.com.rfrench.jvm.simulated_programs;

public class ArithmeticTest 
{
    public static void main(String[]args)
    {
        int a = 24;
        int b = 4;
        
        int total = 0;
        
        total = a + b;
        
        total = a - b;
        
        total = a * b;
        
        total = a / b;
        
        total = a * -1;
        
        total = a % b;
        
        total++;
        ++total;
        
        total--;
        --total;
    }
}
